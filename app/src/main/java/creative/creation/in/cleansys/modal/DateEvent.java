package creative.creation.in.cleansys.modal;

public class DateEvent {
    String s_datel;
    String title;
    String name;

    public DateEvent() {
    }

    public String getS_datel() {
        return s_datel;
    }

    public void setS_datel(String s_datel) {
        this.s_datel = s_datel;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
